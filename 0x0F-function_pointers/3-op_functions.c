#include "3-calc.h"

/**
 * op_add - adds 2 integers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: integer
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts 1 integer from another
 * @a: integer 1
 * @b: integer 2
 *
 * Return: integer
 */
int oop_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies 2 integers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides 1 integer by another
 * @a: integer 1
 * @b: integer 2
 *
 * Return: integer
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the remainder of the division of a by b
 * @a: integer 1
 * @b: integer 2
 *
 * Return: integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
